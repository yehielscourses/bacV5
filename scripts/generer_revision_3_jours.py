def md_to_html_simple(md: str) -> str:
    """Conversion Markdown minimale sans dépendance externe."""
    import html
    import re

    lines = md.split("\n")
    out = []
    in_table = False
    in_ul = False
    in_ol = False
    in_blockquote = False

    def close_lists():
        nonlocal in_ul, in_ol
        if in_ul:
            out.append("</ul>")
            in_ul = False
        if in_ol:
            out.append("</ol>")
            in_ol = False

    def close_bq():
        nonlocal in_blockquote
        if in_blockquote:
            out.append("</blockquote>")
            in_blockquote = False

    def inline(s: str) -> str:
        s = html.escape(s)
        s = re.sub(r"\*\*(.+?)\*\*", r"<strong>\1</strong>", s)
        s = re.sub(r"\*(.+?)\*", r"<em>\1</em>", s)
        s = re.sub(r"`(.+?)`", r"<code>\1</code>", s)
        return s

    i = 0
    while i < len(lines):
        line = lines[i]
        stripped = line.strip()

        if stripped == "---":
            close_lists()
            close_bq()
            if in_table:
                out.append("</table>")
                in_table = False
            out.append("<hr>")
            i += 1
            continue

        if stripped.startswith("|") and "|" in stripped[1:]:
            close_lists()
            close_bq()
            cells = [c.strip() for c in stripped.split("|")[1:-1]]
            if not in_table:
                out.append("<table>")
                in_table = True
                tag = "th" if i + 1 < len(lines) and re.match(r"^\|[\s\-:|]+\|$", lines[i + 1].strip()) else "td"
                if tag == "th":
                    out.append("<tr>" + "".join(f"<th>{inline(c)}</th>" for c in cells) + "</tr>")
                    i += 2
                    continue
                else:
                    out.append("<tr>" + "".join(f"<td>{inline(c)}</td>" for c in cells) + "</tr>")
            else:
                if re.match(r"^\|[\s\-:|]+\|$", stripped):
                    i += 1
                    continue
                out.append("<tr>" + "".join(f"<td>{inline(c)}</td>" for c in cells) + "</tr>")
            i += 1
            continue
        elif in_table:
            out.append("</table>")
            in_table = False

        if stripped.startswith("#### "):
            close_lists()
            close_bq()
            out.append(f"<h4>{inline(stripped[5:])}</h4>")
        elif stripped.startswith("### "):
            close_lists()
            close_bq()
            out.append(f"<h3>{inline(stripped[4:])}</h3>")
        elif stripped.startswith("## "):
            close_lists()
            close_bq()
            if stripped.startswith("## JOUR") or stripped.startswith("## PARTIE"):
                out.append('<div class="page-break"></div>')
            out.append(f"<h2>{inline(stripped[3:])}</h2>")
        elif stripped.startswith("# "):
            close_lists()
            close_bq()
            out.append(f"<h1>{inline(stripped[2:])}</h1>")
        elif stripped.startswith("> "):
            close_lists()
            if not in_blockquote:
                out.append("<blockquote>")
                in_blockquote = True
            out.append(f"<p>{inline(stripped[2:])}</p>")
        elif re.match(r"^[-*] ", stripped):
            close_bq()
            if not in_ul:
                close_lists()
                out.append("<ul>")
                in_ul = True
            out.append(f"<li>{inline(stripped[2:])}</li>")
        elif re.match(r"^\d+\. ", stripped):
            close_bq()
            if not in_ol:
                close_lists()
                out.append("<ol>")
                in_ol = True
            out.append(f"<li>{inline(re.sub(r'^\d+\.\s*', '', stripped))}</li>")
        elif stripped == "":
            close_lists()
            close_bq()
        else:
            close_lists()
            close_bq()
            out.append(f"<p>{inline(stripped)}</p>")
        i += 1

    close_lists()
    close_bq()
    if in_table:
        out.append("</table>")
    return "\n".join(out)


